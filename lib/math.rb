def addition(num1, num2)
  total = num1 + num2
end

def subtraction(num1, num2)
  total = num1 - num2
end

def division(num1, num2)
  total = (num1 / num2)
end

def multiplication(num1, num2)
  total = (num1 * num2)
end

def modulo(num1, num2)
  total = num1 % num2
end

def square_root(num)
  total = Math.sqrt(num)
end