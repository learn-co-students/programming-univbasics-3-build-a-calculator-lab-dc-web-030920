def addition(num1, num2)
  sum = num1 + num2
  puts sum
  sum 
end

def subtraction(num1, num2)
  subtract = num1 - num2
  puts subtract
  subtract
end

def division(num1, num2)
  divide = num1 / num2
  puts divide
  divide
end

def multiplication(num1, num2)
  multiply = num1 * num2
  puts multiply 
  multiply
end

def modulo(num1, num2)
  remainder = num1 % num2
  puts remainder
  remainder
end

def square_root(num)
  square = Math.sqrt(num)
  puts square
  square 
end